#include<stdio.h>
#include<stdlib.h>
int main()
{
	char c;
	int letters=0,space=0,digit=0,other=0;
	printf("请输入一行字符:\n");
	do
	{
		c=getchar();
		if(c>='a'&&c<='z'||c>='A'&&c<='Z')
			letters ++;
		else if(c==' ')
			space ++;
		else if(c>='0'&&c<='9')
			digit ++;
		else
			other ++;
	}while(c!='\n');
	printf("字母数:%d\n空格数:%d\n数字数:%d\n其他字符数:%d\n",letters,space,digit,other);
    system("pause");
	return 0;
}
